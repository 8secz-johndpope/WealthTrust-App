    }
    
    class func TradeGothicLTBoldNo2(size: CGFloat) -> UIFont {
        return UIFont(name: "Trade Gothic LT Bold No. 2", size: size)!
    }
    
    class func TradeGothicLTCondensedNo18(size: CGFloat) -> UIFont {
